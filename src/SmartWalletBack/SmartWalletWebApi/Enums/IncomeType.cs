public enum IncomeType
{
    Salary, // Зарплата
    Scholarship, // Стипендия
    Freelance, // Подработка, фриланс
    Investment, // Дивиденды, проценты по вкладам
    Gift, // Подарок, перевод от родственников
    Sale, // Продажа чего-то (например, вещей или машины)
    Refund, // Возврат (например, от магазина или налоги)
    Other, // Прочее
}
